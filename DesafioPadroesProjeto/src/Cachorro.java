public class Cachorro {

    private String nome;
    private String raca;
    private int idade;
    private String genero;
    private String cor;

    public Cachorro(String nome, String raca, int idade, String genero, String cor) {
        this.nome = nome;
        this.raca = raca;
        this.idade = idade;
        this.genero = genero;
        this.cor = cor;
    }
}
